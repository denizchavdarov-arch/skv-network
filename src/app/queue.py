def generate_report(data):
    """Generate Markdown report from project data."""
    title = data.get("title", "Report")
    rules = data.get("rules", [])
    consultation = data.get("_consultation", data.get("consultation", []))
    if isinstance(consultation, dict):
        consultation = [consultation]
    
    md = f"# {title}\n\n"
    md += f"*Generated by SKV Network*\n\n"
    
    if rules:
        md += "## Project Description\n\n"
        for r in rules:
            md += f"- {r}\n"
        md += "\n"
    
    if consultation:
        md += "## AI Analysis\n\n"
        for c in consultation:
            if c.get("answer"):
                md += f"### {c.get('model', 'AI')}\n\n{c['answer']}\n\n"
    
    name = data.get("cube_id", "report") + ".md"
    with open("/app/uploads/" + name, "w") as f:
        f.write(md)
    return {"type": "md", "url": "/download/" + name}

def convert_json_csv(data):
    """Convert JSON data to CSV."""
    import csv, io
    items = data.get("data", data.get("rules", []))
    if not items:
        return {"error": "No data to convert"}
    
    output = io.StringIO()
    if isinstance(items[0], dict):
        writer = csv.DictWriter(output, fieldnames=items[0].keys())
        writer.writeheader()
        writer.writerows(items)
    else:
        writer = csv.writer(output)
        for item in items:
            writer.writerow([item])
    
    name = data.get("cube_id", "data") + ".csv"
    with open("/app/uploads/" + name, "w") as f:
        f.write(output.getvalue())
    return {"type": "csv", "url": "/download/" + name}
