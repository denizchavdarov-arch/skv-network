"""Simple task queue for SKV — runs in background thread."""
import json, urllib.request, time, threading, uuid

POLZA_KEY = "pza_LjwfFSaGiO54TOspk13LNRyXA_xhufNR"
tasks = {}
results = {}

def add_task(query, model="deepseek-v4-flash"):
    """Add task to queue, return task_id."""
    task_id = str(uuid.uuid4())[:12]
    tasks[task_id] = "queued"
    
    def process():
        tasks[task_id] = "processing"
        body = json.dumps({
            "model": model,
            "messages": [{"role": "user", "content": query}],
            "temperature": 0.5, "max_tokens": 500
        }).encode()
        
        for attempt in range(3):
            try:
                req = urllib.request.Request("https://api.polza.ai/v1/chat/completions", data=body, headers={
                    "Content-Type": "application/json",
                    "Authorization": f"Bearer {POLZA_KEY}"
                })
                resp = urllib.request.urlopen(req, timeout=90)
                answer = json.loads(resp.read())["choices"][0]["message"]["content"]
                if answer:
                    results[task_id] = {"status": "done", "answer": answer}
                    return
            except Exception as e:
                if attempt == 2:
                    results[task_id] = {"status": "error", "error": str(e)[:200]}
        results[task_id] = {"status": "error", "error": "Empty"}
    
    threading.Thread(target=process, daemon=True).start()
    return task_id

def get_status(task_id):
    """Check task status."""
    if task_id in results:
        return results[task_id]
    if task_id in tasks:
        return {"status": tasks[task_id]}
    return {"status": "not_found"}

def generate_files(data):
    """Generate files from data. Returns list of file dicts."""
